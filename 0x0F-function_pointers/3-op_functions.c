#include "3-calc.h"
/**
 * op_add - addition
 * @a: the first number
 * @b: the second number
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - addition
 * @a: the first number
 * @b: the second number
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - addition
 * @a: the first number
 * @b: the second number
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - addition
 * @a: the first number
 * @b: the second number
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - addition
 * @a: the first number
 * @b: the second number
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
